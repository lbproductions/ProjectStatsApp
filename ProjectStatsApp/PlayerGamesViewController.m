#import "PlayerGamesViewController.h"

@implementation PlayerGamesViewController

@synthesize m_player;
@synthesize m_context;

NSMutableArray *keyList;

- (id)initWithNibName:(NSString *)nibNameOrNil bundle:(NSBundle *)nibBundleOrNil player:(NSManagedObject*)player context:(NSString*)context displayTitle:(NSString*)displayTitle
{
    self = [super initWithNibName:nibNameOrNil bundle:nibBundleOrNil];
    if (self) {
        self.m_player = player;
        self.m_context = context;
        NSString* title = displayTitle;
        title = [title stringByAppendingString:@" of "];
        title = [title stringByAppendingString:(NSString*)[player valueForKey:@"name"]];
        self.title =  title;
        
        keyList = [[NSMutableArray alloc] init];
        
        NSMutableDictionary* dict = (NSMutableDictionary*)[self.m_player valueForKey:self.m_context];
        NSEnumerator *enumerator = [dict keyEnumerator];
        id object;
        
        while ((object = [enumerator nextObject])) {
            [keyList addObject:object];
        }
    }
    return self;
}

